public class Exercicio1ProfessorIA {
    public static void main(String[] args) {
        int numero1 = 5;
        int numero2 = -5;
        System.out.println("A soma dos dois numeros é: " + (numero1 + numero2));
        System.out.println("A diferença os dois numeros é: " + (numero1 - numero2));
        System.out.println("O produto de dois numeros é: " + (numero1 * numero2));
        if (numero2 > 0) {
            System.out.println("O quociente da divisão de dois numeros é: " + (numero1 + numero2));
        } else {
            System.out.println("Não é possível realizar essa equação.");
        }
        
    }
    
}
