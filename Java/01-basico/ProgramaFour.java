import java.util.*;

public class ProgramaFour {
    public static void main(String[] args) {
      int idade = 25;
      double altura = 1.80;
      boolean maiorIdade = true;
      char inicial = 'A';
      
      System.out.println("Trabalhando com dados Primitivos - inteiro");
      System.out.println(idade);
      System.out.println("Trabalhando com dados Primitivos - double");
      System.out.println(altura);
      System.out.println("Trabalhando com dados Primitivos - boolean");
      System.out.print("Você é maior de idade? ");
      System.out.println(maiorIdade);
      System.out.println("Trabalhando com dados Primitivos - char");
      System.out.println(inicial);
  }
}