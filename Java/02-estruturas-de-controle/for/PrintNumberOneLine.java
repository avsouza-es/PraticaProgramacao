import java.util.*;

public class PrintNumberOneLine {
    public static void main(String[] args) {
      
      int i;
      
      for(i=0;i<=10;i++){
        System.out.println(i);  
      }
      
      System.out.println();
      for(int x=0;x<=10;x++){
        System.out.print(x + " ");  
      }
      
  }
}