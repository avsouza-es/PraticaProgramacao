public class ExemploUsoArrayStrings {
	public static void main(String[] args) {
		// Declarando e inicializando um array de strings
		String[] nomes = {"Alice", "Ana", "Marcela", "Giselia"};

		// Imprimindo os elementos do array
		for (int i= 0; i < nomes.length; i++) {
			System.out.println("Nome " + i + ": " + nomes[i]);
		}
	}	
}
